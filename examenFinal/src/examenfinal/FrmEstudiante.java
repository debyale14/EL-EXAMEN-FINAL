   public void actualizar(){
        try{ 
            CBaseDatos con=new CBaseDatos();
            ArrayList datos=new ArrayList();
            
            ResultSet rs = con.select(
            "SELECT id,nombre,carrera FROM estudiantes;"
            );

            while(rs.next()){
                datos.add( new Object []  { rs.getString(1),rs.getString(2),rs.getString(3)} );
            }
            
            int n=datos.size();
            Object[][] data = new Object[n][3];
            
            for(int i=0;i<=n-1;i++){
                //datos.get(i);
                data[i][0]=((Object[])datos.get(i))[0];
                data[i][1]=((Object[])datos.get(i))[1];
                data[i][2]=((Object[])datos.get(i))[2];
            }       
            
            datos.clear();
            
            TabEstudiantes.setModel(new javax.swing.table.DefaultTableModel(
                   data
                    ,
                new String [] {
                    "nombre", "id","carrera"
                }
            ));

        }catch(Exception e){ 
            System.out.println(e);
        }

    }
